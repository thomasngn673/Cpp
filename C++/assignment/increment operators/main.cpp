#include <iostream>
using namespace std;

int main() {
    int x = 10;
    x+=5;
    // x+=5 means x+5
    // x-=5 means x-5
    // x*=5 means x*5
    // x/= means x/5
    // x%= means division with remainder
    cout << x << endl;
    // x++ runs the code, THEN adds 1 to the x AFTER it
    // ++x adds 1 to x BEFORE it, THEN runs the code
}
